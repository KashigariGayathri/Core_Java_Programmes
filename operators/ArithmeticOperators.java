package operators;

public class ArithmeticOperators {

	public static void main(String[] args) {
		int a=8;
		int b=2;
		System.out.println("Adition: "+(a+b));
		System.out.println("Subtraction: "+(a-b));
		System.out.println("Multiplication: "+(a*b));
		System.out.println("Division: "+(a/b));
		System.out.println("Adition: "+(a%b));
	
	}

}
